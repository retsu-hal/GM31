#pragma once
#include "Vector3.h"

//=====================================================================================
// 当たり判定専用の静的ユーティリティクラス
// GameObjectに依存せず、座標・サイズ(Vector3)だけで判定できるようにしてある。
// Box(AABB)・Enemy(球/円)・Tree(円柱)など色々な形状の当たり判定をここに集約する。
//=====================================================================================
class Collision
{
public:
	//-------------------------------------------------------------------------------
	// AABB(直方体) vs AABB
	// posA / scaleA : 押し出される側（Player等）の中心座標とサイズ
	// posB / scaleB : ぶつかる相手（Box等）の中心座標とサイズ
	// outPushVector : めり込みを解消するための最小移動ベクトル（posAに加算する）
	// outIsGround   : 上面に着地した場合 true になる（Y軸で上方向に押し出された場合）
	// 戻り値        : 当たっていればtrue
	//-------------------------------------------------------------------------------
	static bool AABB(const Vector3& posA, const Vector3& scaleA, const Vector3& posB, const Vector3& scaleB, Vector3& outPushVector, bool& outIsGround);

	//-------------------------------------------------------------------------------
	// 球 vs 球（3D距離判定）
	// Bulletの命中判定など、厳密な押し出しが不要な単純な当たり判定に使用
	//-------------------------------------------------------------------------------
	static bool Sphere(const Vector3& posA, float radiusA, const Vector3& posB, float radiusB);

	//-------------------------------------------------------------------------------
	// 円柱（XZ平面のみの円判定。Y軸は無視する）
	// Treeのような「高さは気にせず、上から見た円で判定したい」ケース用
	// outPushVector : めり込みを解消するための移動ベクトル（Y成分は常に0）
	//-------------------------------------------------------------------------------
	static bool Circle2D(const Vector3& posA, float radiusA, const Vector3& posB, float radiusB, Vector3& outPushVector);

	//-------------------------------------------------------------------------------
	// 点 vs 球（Bulletの座標そのものとEnemyの当たり判定など）
	//-------------------------------------------------------------------------------
	static bool PointInSphere(const Vector3& point, const Vector3& center, float radius);
};